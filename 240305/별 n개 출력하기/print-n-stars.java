import java.util.*;

public class Main {
    public static void main(String[] args) {
        // 여기에 코드를 작성해주세요.
        Scanner sc = new Scanner(System.in);

        // n 입력 받기 
        int n = sc.nextInt();

        //  n개의 줄에 걸쳐 "*" 하나씩 출력
        while(n-- > 0){
            System.out.println("*");
        }
    }
}