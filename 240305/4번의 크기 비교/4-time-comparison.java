import java.util.*;

public class Main {
    public static void main(String[] args) {
        // 여기에 코드를 작성해주세요.
        Scanner sc = new Scanner(System.in);

        // 입력 받기
        int a = sc.nextInt();
        int b = sc.nextInt();
        int c = sc.nextInt();
        int d = sc.nextInt();
        int e = sc.nextInt();

        // a > b 비교
        if( a > b ) {
            System.out.println("1");
        } else {
            System.out.println("0");
        }

        // a > c 비교
        if( a > c ) {
            System.out.println("1");
        } else {
            System.out.println("0");
        }

        // a > d 비교
        if( a > d ) {
            System.out.println("1");
        } else {
            System.out.println("0");
        }

        // a > e 비교
        if( a > e ) {
            System.out.println("1");
        } else {
            System.out.println("0");
        }
    
    }
}